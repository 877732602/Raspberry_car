class CameraMove(object):

    def __init__(self):
        pass

    def rate_left(self):
        print('摄像头向左旋转')

    def rate_up(self):
        print('摄像头向上旋转')

    def rate_down(self):
        print('摄像头向下旋转')

    def rate_right(self):
        print('摄像头向右旋转')

    def rate_stop(self):
        print('摄像头停止旋转')
